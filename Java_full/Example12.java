package com.ust.examples;

public class Example12 {

	public static void main(String[] args) {
		char ch = 'b';
		switch(ch){
		
			case 'a':
				System.out.println("Ant");
				break;
				
			case 'b':
				System.out.println("Bat");
				break;
				
			case 'c':
				System.out.println("Cat");
				break;
				
			case 'd':
				System.out.println("Dog");
				break;
				
			default:
				System.out.println("Animals");
		}
		

	}

}
